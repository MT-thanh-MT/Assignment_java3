    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        String maSV = txtMa.getText();
        if (maSV.equals("")) {
            maSV = JOptionPane.showInputDialog("Nhập mã sinh viên muốn xóa");
            if (maSV == null)return;
        }
        int xn = JOptionPane.showConfirmDialog(this, "Ban có muốn Xóa bảng điểm của sinh viên này không?");
        if (xn == JOptionPane.YES_OPTION) {
            try {
                if (this.qld.timTheoMa(getThongTinhSV().getMASV()) != null) {
                    this.qld.delete(maSV);
                    JOptionPane.showMessageDialog(this, "Đã xóa thành công");
                    fillTable(cbbHienThi.getSelectedIndex());
                    clearForm();
                } else {
                    JOptionPane.showMessageDialog(this, "Mã sinh viên này không tồn tại");
                    return;
                }

            } catch (Exception ex) {
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

