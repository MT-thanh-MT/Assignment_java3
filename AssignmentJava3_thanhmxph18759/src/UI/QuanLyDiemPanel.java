    public void fillTable(int choice) {
        this.dtm.setRowCount(0);
        ArrayList<SinhVien> list = new ArrayList<>();
        switch (choice) {
            case 0: {
                try {
                    list = this.qld.getDanhSachSV();
                    addDataForTable(list);
                } catch (Exception ex) {
                    Logger.getLogger(QuanLyDiemPanel.class.getName()).log(Level.SEVERE, null, ex);
                }
            }

            break;

            case 1:
                int top = Integer.parseInt(JOptionPane.showInputDialog("Bạn Muốn tìm top mấy?"));
                 {
                    try {

                        list = this.qld.getTopDanhSachSV(top);
                        addDataForTable(list);
                    } catch (Exception ex) {
                        ex.printStackTrace();
                    }
                }
                break;

        }

    }

    public void addDataForTable(ArrayList<SinhVien> list) {
        for (SinhVien sv : list) {
            float dtb = (sv.getTiengAnh() + sv.getTinHoc() + sv.getGDTC()) / 3;
            Object[] data = new Object[]{
                sv.getMASV(),
                sv.getHoTen(),
                sv.getTiengAnh(),
                sv.getTinHoc(),
                sv.getGDTC(),
                String.format("%.2f", dtb)
            };
            this.dtm.addRow(data);
        }
    }

    public void fillForm(String maSV) {
        SinhVien sv = new SinhVien();
        try {
            sv = this.qld.timTheoMa(maSV);
            if (sv == null) {
                JOptionPane.showMessageDialog(this, "Mã Sinh Viên Không Tồn Tại");
                return;
            }
            this.txtMa.setText(sv.getMASV());
            this.lblSetHoTen.setText(sv.getHoTen());
            this.txtTiengAnh.setText(String.valueOf(sv.getTiengAnh()));
            this.txtTinHoc.setText(String.valueOf(sv.getTinHoc()));
            this.txtGDTC.setText(String.valueOf(sv.getGDTC()));
            float dtb = (sv.getGDTC() + sv.getTiengAnh() + sv.getTinHoc()) / 3;
            this.lblHienThiDTB.setText(String.format("%.2f", dtb));

        } catch (Exception ex) {
            ex.printStackTrace();
        }

    }

