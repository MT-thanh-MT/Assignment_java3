    public SinhVien getThongTinhSV() {
        String maSV,HoTen;
        HoTen = this.lblSetHoTen.getText();
        maSV = this.txtMa.getText();
        float dTiengAnh = Float.parseFloat(this.txtTiengAnh.getText());
        float dTinHoc = Float.parseFloat(this.txtTinHoc.getText());
        float dGDTC = Float.parseFloat(this.txtGDTC.getText());
        
        SinhVien sv = new SinhVien();
        sv.setTiengAnh(dTiengAnh);
        sv.setTinHoc(dTinHoc);
        sv.setGDTC(dGDTC);
        sv.setHoTen(HoTen);
        sv.setMASV(maSV);
        
        return sv;
    }
