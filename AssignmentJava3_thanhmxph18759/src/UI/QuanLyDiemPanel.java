    public void doiMauChuTitle() {
        Runnable setColorTitle = new Runnable() {
            @Override
            public void run() {
                int i = 0;
                while (true) {
                    if (i > 15) {
                        i = 0;
                    }
                    if (i == 0) {
                        lblTitle.setForeground(Color.red);
                    }

                    if (i == 5) {
                        lblTitle.setForeground(Color.BLUE);
                    }

                    if (i == 10) {
                        lblTitle.setForeground(Color.black);
                    }

                    i++;
                    try {
                        Thread.sleep(300);
                    } catch (InterruptedException ex) {
                        Logger.getLogger(QuanLySinhVienPanel.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }

            }
        };

        Thread t = new Thread(setColorTitle);
        t.start();
    }

    public void GioHeThong() {
        new Thread() {
            @Override
            public void run() {
                while (true) {
                    Calendar now = Calendar.getInstance();
                    int h = now.get(Calendar.HOUR);
                    int m = now.get(Calendar.MINUTE);
                    int s = now.get(Calendar.SECOND);

                    lblOclock.setText(h + ":" + m + ":" + s);
                }
            }

        }.start();
    }

    public void chuChay() {
        new Thread() {
            @Override
            public void run() {
                String txt = lblChayChu.getText();
                while (true) {
                    txt = txt.substring(1, txt.length()) + txt.charAt(0);
                    lblChayChu.setText(txt);
                    try {
                        Thread.sleep(320);
                    } catch (InterruptedException ex) {
                        Logger.getLogger(QuanLyDiemPanel.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }

        }.start();
    }

